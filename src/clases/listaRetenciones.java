    public void tabla(){
       ResultSet rs = con.consulta("SELECT Secuencia, NuComprob, CONVERT(VARCHAR,dbo.sfAA_FechaSQL(Fecha),103) AS Fecha FROM ImCmRetIvaCo group by NuComprob, Fecha, Secuencia");
                
        DefaultTableModel data = new DefaultTableModel();        
        listaRetencion.setModel(data);
        data.addColumn("NÂ°");
        data.addColumn("Numero de Comprobante");
        data.addColumn("Fecha");
    try {            
            while (rs.next()) {
                Object[] fila = new Object[3];
                for (int i = 0; i < 3; i++) {
                    fila[i] = rs.getObject(i+1);                    
                }
                data.addRow(fila);
            }            
        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }

